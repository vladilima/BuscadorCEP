public class Endereco {

    String CEP;
    String estado;
    String localidade;
    String bairro;
    String logradouro;

    public Endereco(String CEP, String estado, String localidade, String bairro, String logradouro) {
        this.CEP = CEP;
        this.estado = estado;
        this.localidade = localidade;
        this.bairro = bairro;
        this.logradouro = logradouro;
    }

}
